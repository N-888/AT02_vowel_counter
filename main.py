# Функция для подсчёта количества гласных букв в строке
def count_vowels(s: str) -> int:
    # Множество гласных букв (английских) в нижнем регистре
    vowels = set('aeiou')
    # Преобразуем строку в нижний регистр, чтобы считать и прописные, и строчные
    s_lower = s.lower()
    # Счётчик гласных
    count = 0
    # Перебираем каждый символ в строке
    for char in s_lower:
        # Если символ является гласной, увеличиваем счётчик
        if char in vowels:
            count += 1
    # Возвращаем итоговое количество
    return count